let a = true;
let b = false;
let c = true;
let d = false;

// OR = Bernilai true apabila salah satu ada yang bernilai true
console.log( d || b || a );

// AND = Bernilai false jika salah satu ada yang bernilai false
console.log( a && b && c);

let x = 5;
let y = 2;

// Lebih besar dari
console.log(x > y);

// Lebih kecil dari
console.log(x < y);

// Sama dengan
console.log(x == y);

// Lebih besar dari sama dengan
console.log(x >= y);

// Kurang dari sama dengan
console.log(x <= y);

